
/**
 * The Container interface, used to represent the role of container.
 * @author Chen Hung-Yu
 */
public interface Container {
	/**
	 * Get the capacity of container.
	 * @return the capacity of container.
	 */
	public abstract int getCapacity();
	/**
	 * Get the display name of container.
	 * @return the display name of container.
	 */
	public abstract String getConName();
}
